--the average rate of change of a function over two the interval between two points. xtwo corresponds to f(b) and xone corresponds to
--f(a). ytwo corresponds to b and yone to a. 

averageRateOfChange xone xtwo yone ytwo = (xtwo - xone) / (ytwo - yone)



